    public int run() {
        while (ip < expr.length) {
            step();
        }

        return stack.pop();
    }

    public void step() {
        Item nxt = expr[ip++];

        int y;
        int x;

        switch (nxt.type()) {
            case VALUE:
                stack.push(nxt.value());
                break;

            case ADD:
                y = stack.pop();
                x = stack.pop();
                stack.push(x + y);
                break;

            case SUB:
                y = stack.pop();
                x = stack.pop();
                stack.push(x - y);
                break;

            case MUL:
                y = stack.pop();
                x = stack.pop();
                stack.push(x * y);
                break;

            case DIV:
                y = stack.pop();
                x = stack.pop();
                stack.push(x / y);
                break;
        }
    }
